#!/usr/bin/env python3
"""Export adversarial multi-turn sessions into a TypeScript data file
for the multi-turn arena web UI.

Reads the adversarial multiturn results JSON (56 sessions, 7 models × 8
seeds × 12 turns) and writes web/lib/multiturn-data.ts containing the
sessions and a pre-computed list of canonical pairings.

Usage:
    python3 scripts/export_multiturn_arena.py [results_json]
"""
import json
import sys
from itertools import combinations
from pathlib import Path

SRC = Path("results/multiturn_20260414_042100.json")
OUT = Path("web/lib/multiturn-data.ts")


def main():
    src = Path(sys.argv[1]) if len(sys.argv) > 1 else SRC
    data = json.load(open(src))

    sessions = []
    for s in data["sessions"]:
        if "judges" not in s or not s.get("dialogue"):
            continue
        sessions.append({
            "seed_id": s["seed_id"],
            "test_model": s["test_model"],
            "character_name": s.get("character_name", "Character"),
            "user_name": s.get("user_name", "User"),
            "num_turns": s.get("num_turns", len(s["dialogue"])),
            "dialogue": [
                {
                    "role": "assistant" if m["role"] in ("assistant", "character") else "user",
                    "content": m["content"],
                }
                for m in s["dialogue"]
            ],
        })

    # Build canonical pairs: for each seed, every C(n,2) model combination
    by_seed: dict[str, list[str]] = {}
    for s in sessions:
        by_seed.setdefault(s["seed_id"], []).append(s["test_model"])

    pairs = []
    for seed, models in sorted(by_seed.items()):
        for a, b in combinations(sorted(set(models)), 2):
            pair_id = f"mt_{seed}_{a}_vs_{b}"
            pairs.append({
                "id": pair_id,
                "seed_id": seed,
                "model_a": a,
                "model_b": b,
            })

    # Seed metadata from adversarial seeds JSON
    seed_meta_path = Path("hf_dataset/_source/adversarial_seeds.json")
    seed_meta = {}
    if seed_meta_path.exists():
        adv_seeds = json.load(open(seed_meta_path))
        for s in adv_seeds:
            setting = s.get("character_setting", "")
            # Truncate to a usable summary (~200 chars)
            if len(setting) > 200:
                setting = setting[:197] + "..."
            seed_meta[s["id"]] = {
                "failure_target": s.get("failure_target", ""),
                "setting_summary": setting,
                "character_name": s.get("character_name", ""),
                "user_name": s.get("user_name", ""),
            }

    ts = (
        '// Auto-generated by scripts/export_multiturn_arena.py. Do not edit.\n'
        'import { MultiturnSession, MultiturnPair, SeedMeta } from "./multiturn-types";\n\n'
        f"export const MULTITURN_SESSIONS: MultiturnSession[] = {json.dumps(sessions, indent=2)};\n\n"
        f"export const MULTITURN_PAIRS: MultiturnPair[] = {json.dumps(pairs, indent=2)};\n\n"
        f"export const SEED_METADATA: Record<string, SeedMeta> = {json.dumps(seed_meta, indent=2)};\n"
    )

    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(ts)
    print(f"Exported {len(sessions)} sessions, {len(pairs)} pairs to {OUT}")
    print(f"File size: {OUT.stat().st_size / 1024:.0f} KB")


if __name__ == "__main__":
    main()
